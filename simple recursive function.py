def srf(a, b):
    if a == b:                         # базовый случай, функция перестает себя вызывать
        return f'{a}'
    if a < b:                          # следующий шаг, постепенно упрощаем задачу, пока не
        return f'{a}, {srf(a + 1, b)}' #  придем к базовому случаю
    if a > b:
        return f'{a}, {srf(a - 1, b)}'


print(srf(1, 4))
print(srf(5, 1))